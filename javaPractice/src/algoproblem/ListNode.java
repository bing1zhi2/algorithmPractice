public class ListNode{
    int val;
    ListNode next;
    ListNode(int val){
        this.val=val;
    }

    @Override
    public String toString() {
        return "algoproblem.ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
